# Calcular a área de um triângulo a partir de sua base e altura.

print ('Informe os dados do triângulo') #Escreve na tela para usuario.
base = float (input ('Base: ')) #Variavel recebendo numero decimal.
altura = float (input ('Altura: ')) #Variavel recebendo numero decimal.
area = base * altura / 2 #vaiavel recebendo o calculo da area do triangulo.
print ('A area do triângulo é ', area, ' m².') #Informa o resultado na tela para usuario.